public class PerfilMascota {

    private int foto2;
    private String nombre2;
    private int numlikes2 ;

    public PerfilMascota(int foto2, String nombre2, int numlikes2){
        this.foto2 = foto2;
        this.nombre2 = nombre2;
        this.numlikes2 = numlikes2;
    }

    public int getFoto2() {
        return foto2;
    }

    public void setFoto2(int foto2) {
        this.foto2 = foto2;
    }

    public String getNombre2() {
        return nombre2;
    }

    public void setNombre2(String nombre2) {
        this.nombre2 = nombre2;
    }

    public int getNumlikes2() {
        return numlikes2;
    }

    public void setNumlikes2(int numlikes2) {
        this.numlikes2 = numlikes2;
    }




}
